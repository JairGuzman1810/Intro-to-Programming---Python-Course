# The help() function is possibly the most important
# Python function you can learn. If you can remember how
# to use help(), you hold the key to understanding most other functions.
help(round)

# Python evaluates an expression like this from the inside out. First it calculates
# the value of round(-2.01), then it provides help on the output of that expression.
help(round(-2.01))

# Help on built-in function print in module builtins
help(print)
